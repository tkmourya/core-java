public class ArrayForEach {
    public static void main(String[] args) {
        double[] d = {1.56,3.99,4.83,5.3};
        String[] p ={"virat","rohit","dhoni","raina"};
        int[] n = {1,2,3,5,6,7};
        for(double dd:d){
            System.out.println(dd);
        }
        for(String ss:p){
            System.out.println(ss);
        }
        for(int ii:n){
            System.out.println(ii);
        }
    }
}
