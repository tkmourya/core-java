public class StringMethod5
{
    public static void main(String[] args) {
        String s1 = "hello";
        String s2 = "hero";
        System.out.println(s1.compareTo(s2));
        System.out.println("hello".compareTo("i"));
        System.out.println("abcd".compareTo("ABCD"));
        System.out.println("KOD".compareTo("KO"));
    }
}
